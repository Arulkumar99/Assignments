package bytes;

class Bytes
{
	public static void main(String[] args)
	{
	byte a=99;             // byte value ranges from -128 to 127;
	int b=(int) a; 
	System.out.println(" The integer into  byte value is :"+b);
	byte c=111;
	int d=(int) c;
	System.out.println(" The integer into  byte value is :"+d);
	int e=c+d;
	System.out.println(" The adding two byte value is:"+e);
	  
	}
}
